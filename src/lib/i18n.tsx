type I18nContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>("es");

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale,
      t: (key) => dictionaries[locale][key] ?? key
    }),
    [locale]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }

  return context;
}

/** Returns the localised name of a boat system object. */
export function sysName(
  s: { nameEs?: string; nameEn?: string; systemCode?: string },
  locale: Locale
): string {
  if (locale === "en") return s.nameEn || s.nameEs || s.systemCode || "";
  return s.nameEs || s.nameEn || s.systemCode || "";
}
